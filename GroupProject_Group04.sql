--CREATING CONTACT TABLE BY MQ
CREATE TABLE CONTACT
(NAME VARCHAR2(20),
PHONE# NUMBER(10),
EMERGANCY NUMBER(10),
RELATION VARCHAR2(10),
EMAIL VARCHAR2(30),
    CONSTRAINT CONTACT_CONTACT#_PK PRIMARY KEY(PHONE#));

--CREATING DEPARTMENT TABLE BY MQ
CREATE TABLE DEPARTMENT
(DEPARTMENT_ID NUMBER(10),
NAME VARCHAR2(25) NOT NULL,
ADDRESS VARCHAR2(25),
PHONE# NUMBER(10),
    CONSTRAINT DEPARTMENT_DEPARTMENT_ID_PK PRIMARY KEY(DEPARTMENT_ID),
    CONSTRAINT DEPARTMENT_PHONE#_FK FOREIGN KEY(PHONE#)
        REFERENCES CONTACT(PHONE#));

--CREATING CRIME TABLE BY MQ
CREATE TABLE CRIME
(CRIME_ID NUMBER(10),
TYPE VARCHAR2(15) NOT NULL,
CRIMINALS NUMBER(2),
WEAPONS NUMBER(2),
OFFICERS NUMBER(2),
ADDRESS VARCHAR(25),
CRIME_DATE DATE,
    CONSTRAINT CRIME_CRIME_ID_PK PRIMARY KEY(CRIME_ID));

--Creating CRIMINAL TABLE BY AD
