WHERE CRIMINALS > 1;

--SHOW ME THE CRIMES HAPPEND IN THE FIRST QUARTER OF 2024
SELECT * FROM crime
WHERE crime_date BETWEEN '01-JAN-24' AND '31-MAR-24';

--FIND THE ADDRESS AND THE PHONE NUMBER FOR THE ALIVE VICTIMS
SELECT F_NAME, ADDRESS, PHONE#
    FROM VICTIM
        WHERE STATUS IN ('UNHARMED','INJURED');
        
--FIND THE EMERGENCY DETAILS WITH CODE 647
SELECT NAME, EMERGENCY, RELATION
    FROM CONTACT
        WHERE EMERGENCY LIKE '647%';
        
--SHOW THE CASES TAKEN BY JUDGE RYAN WHICH IS CLIODSED
SELECT CASE#, CRIMINAL_ID, JUDGE, STATUS
    FROM CASE
        WHERE STATUS = 'PENDING' AND JUDGE LIKE 'RYAN%';
        
--SHOW CASES TAKEN BY RYAN OR ANY CASE THAT IS PENDING
SELECT CASE#, CRIMINAL_ID, JUDGE, STATUS
    FROM CASE
        WHERE STATUS = 'PENDING' OR JUDGE LIKE 'RYAN%';  

--SHOW THE CRIME RECORDS OF MURDER OR ANY OTHER CRIME WITH MORE THAN 1 CRIMINALS AND IT SHOULD HAVE MORE THAN 1 OFFICER
SELECT * FROM crime
    WHERE (TYPE = 'MURDER'
    OR CRIMINALS > 1)
    AND OFFICERS > 1;

--SHOW THE CRIMINALS WHO ARE NOT BEEN ARRESTED
SELECT CRIME_ID, CRIMINAL_ID, F_NAME, L_NAME
    FROM CRIMINAL
        WHERE ARREST_DATE IS NULL;

--HOW MANY CRIMES SOLVED BY A PARTICULAR OFFICER
SELECT 
    c.CRIME_ID, 
    c.TYPE, 
    c.ADDRESS, 
    c.CRIME_DATE,
    O.BADGE#,
    O.F_NAME,
    O.L_NAME
FROM 
    CRIME c
JOIN 
    OFFICER o
ON 
    c.CRIME_ID = o.CRIME_ID
WHERE 
    o.BADGE# = 9987435;
    
--Shows the murders reported
SELECT * FROM CRIME 
WHERE SUBSTR(CRIME_ID, 1 , 2) = 22;

--Cases that have been closed
SELECT CASE# , VERDICT, SENTENCE
FROM CASE
WHERE STATUS = 'CLOSED';

--Find all officers who were involved in a crime in a specific department
SELECT 
    o.F_NAME, 
    o.L_NAME,
    o.CRIME_ID,
    d.NAME AS DEPARTMENT_NAME
FROM 
    OFFICER o
JOIN 
    DEPARTMENT d ON o.DEPARTMENT_ID = d.DEPARTMENT_ID
WHERE 
    d.NAME = 'BAYVIEW POLICE';
    
--Get the names of criminals and their associated crimes where the crime involved more than 1 weapons
SELECT 
    c.F_NAME, 
    c.L_NAME, 
    cr.TYPE,
    cr.CRIME_ID
FROM 
    CRIMINAL c
JOIN 
    CRIME cr ON c.CRIME_ID = cr.CRIME_ID
WHERE 
    cr.WEAPONS > 1;

--Retrieve the contact details of victims who were injured in a crime 
SELECT 
    v.F_NAME, 
    v.L_NAME, 
    co.PHONE#, 
    co.EMAIL
FROM 
    VICTIM v
JOIN 
    CONTACT co ON v.PHONE# = co.PHONE#
WHERE 
    v.STATUS = 'INJURED';
    
--FIND OUT HOW MANY ARRESTS MADE BY EACH OFFICER
SELECT 
    O.F_NAME + ' ' + O.L_NAME AS NAME, 
    O.BADGE#,
    COUNT(CR.CRIMINAL_ID) AS ARRESTS
FROM 
    OFFICER O
JOIN 
